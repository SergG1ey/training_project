my_list = [5, 8, 9, 3, 6, 7, 4, 1, 2, 5]
length_list = len(my_list)

print(
    "3-й символ строки",
    my_list[2],
    "\nпоследний символ строки",
    my_list[length_list - 1],
    "\nпервые 5 символов строки",
    my_list[0:5],
    "\nвся строка без последних 2-х символов",
    my_list[0:8],
    "\nчетные индексы",
    my_list[::2],
    "\nне четные индексы",
    my_list[1::2],
    "\nобратный порядок",
    my_list[::-1],
    "\nдлина строки",
    length_list
)

